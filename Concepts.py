## 3. Step-by-Step Notes (Bullet Style)

# * Variables are containers used to store data values.
# * No Declaration needed: You do not need to declare data types explicitly.
# * Assignment Operator: The = symbol is used to assign a value to a variable.
# * Case-Sensitive: The variable name age is completely different from Age.
# * Data Types: Python automatically categorizes data into types like integers (int), decimals (float), text (str), and booleans (bool).

