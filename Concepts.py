## 5. Common Mistakes (Exam Tips)

# *  Invalid Names: You cannot start a variable name with a number. (e.g., 1st_name = "Rishabh" will give a syntax error).
# *  No Spaces: Variable names cannot contain spaces. Use underscores instead (e.g., use my_age, not my age).
# *  Special Characters: Do not use symbols like @, $, or % in your variable names.

