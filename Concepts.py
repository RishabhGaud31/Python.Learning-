## 📚 Topic 1: Variables and Data Types in Python## 1. Concept Introduction (Theory First)
# Think of Variables as labeled storage boxes or containers in your computer's memory. You put data inside the box, and you use the label to find it later.
# In languages like C++ or Java, you must tell the computer what kind of data goes into the box beforehand. But in Python, it is much smarter! Python looks at what you put inside the box and automatically figures out the data type. This feature is called Dynamic Typing.

## 2. Syntax Demonstration
